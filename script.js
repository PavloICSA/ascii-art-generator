async function generateASCII() {
    const output = document.getElementById('asciiOutput');
    const info = document.getElementById('outputInfo');

    if (currentTab === 'image') {
        if (!uploadedImage) {
            info.textContent = 'Upload an image first.';
            return;
        }

        output.textContent = 'Processing image...';
        const img = new Image();
        img.onload = () => processImageToASCII(img);
        img.src = uploadedImage;
        return;
    }

    if (currentTab === 'text') {
        await generateTextASCII();
        return;
    }

    if (currentTab === 'prompt') {
        await generateFromPrompt();
    }
}

async function generateTextASCII() {
    const textInput = document.getElementById('textInput').value.trim();
    const style = document.getElementById('styles').value;
    const output = document.getElementById('asciiOutput');
    const outputInfo = document.getElementById('outputInfo');
    
    if (!textInput) {
        output.textContent = '';
        outputInfo.textContent = 'Enter text to generate ASCII art';
        return;
    }
    
    try {
        output.textContent = 'Generating ASCII art...';
        outputInfo.textContent = 'Processing...';
        
        const asciiArt = await createTextASCII(textInput, style);
        
        output.textContent = asciiArt;
        outputInfo.textContent = `Generated with ${style} style ‚Ä¢ ${asciiArt.split('\n').length} lines`;
        
    } catch (error) {
        console.error('Error generating ASCII art:', error);
        output.textContent = 'Error generating ASCII art. Please try again.';
        outputInfo.textContent = 'Generation failed';
    }
}

async function generateFromPrompt() {
    const promptVal = document.getElementById('promptInput').value.trim();
    const output = document.getElementById('asciiOutput');
    const outputInfo = document.getElementById('outputInfo');

    if (!promptVal) {
        outputInfo.textContent = "Please enter a prompt first.";
        return;
    }

    output.textContent = "AI is thinking...";
    outputInfo.textContent = "Generating AI Image...";

    const enhancedPrompt = encodeURIComponent(
        `${promptVal} simple black icon silhouette, white background, high contrast, minimal detail`
    );

    const imageUrl =
        `https://image.pollinations.ai/prompt/${enhancedPrompt}` +
        `?width=512&height=512&nologo=true&seed=${Math.floor(Math.random() * 9999)}`;

    const img = new Image();
    img.crossOrigin = "Anonymous";

    const timeout = setTimeout(() => {
        showServiceBusyMessage();
    }, 8000);

    img.onload = () => {
        clearTimeout(timeout);
        processImageToASCII(img, true);
    };

    img.onerror = () => {
        clearTimeout(timeout);
        showServiceBusyMessage();
    };

    try {
        img.src = imageUrl;
    } catch (err) {
        clearTimeout(timeout);
        console.warn('AI image generation failed:', err);
        showServiceBusyMessage();
    }
}

function processImageToASCII(img, isAI = false) {
    const canvas = document.getElementById('imageCanvas');
    const ctx = canvas.getContext('2d');
    const output = document.getElementById('asciiOutput');
    const outputInfo = document.getElementById('outputInfo');

    const maxWidth = 80;
    const maxHeight = 50;
    
    const charAspectRatio = 0.55; 
    const ratio = Math.min(maxWidth / img.width, (maxHeight / img.height) / charAspectRatio);
    
    canvas.width = Math.floor(img.width * ratio);
    canvas.height = Math.floor(img.height * ratio);
    
    ctx.drawImage(img, 0, 0, canvas.width, canvas.height);
    
    const imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
    const asciiChars = ' .:-=+*#%@';
    let asciiArt = '';
    
    for (let y = 0; y < canvas.height; y++) {
        for (let x = 0; x < canvas.width; x++) {
            const i = (y * canvas.width + x) * 4;
            const avg = (imageData.data[i] * 0.2126 + imageData.data[i+1] * 0.7152 + imageData.data[i+2] * 0.0722);
            const charIndex = Math.floor((avg / 255) * (asciiChars.length - 1));
            asciiArt += asciiChars[charIndex];
        }
        asciiArt += '\n';
    }
    
    output.textContent = asciiArt;
    outputInfo.textContent = isAI ? `AI Prompt Result: ${canvas.width}x${canvas.height}` : `Image Result: ${canvas.width}x${canvas.height}`;
}

